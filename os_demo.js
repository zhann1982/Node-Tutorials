const os = require('os');

// Platform
console.log(os.platform());

// CPU Arch
console.log(os.arch());

// CPU Core Info
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// Home dir
console.log(os.homedir());

// Total memory
console.log(os.totalmem());

// Uptime
console.log(os.uptime());
